def diff(hash1, hash2):
    res1 = bin(int(hash1, 16)).zfill(8)
    res2 = bin(int(hash2, 16)).zfill(8)
    print("{}: {}".format(hash1, res1.replace("0b", "")))
    print("{}: {}".format(hash2, res2.replace("0b", "")))

    counter = 0
    i = 0

    while i < len(res1):
        if res1[i] != res2[i]:
            counter += 1
        i += 1

    print("\tDifferent bits: {}/{}".format(counter, len(res1) - 2))
    print()


